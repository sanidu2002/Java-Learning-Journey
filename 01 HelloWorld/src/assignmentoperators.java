public class assignmentoperators {
    public static void main(String[] args) {

        int x = 10;

        x += 5; // x is now 15
        System.out.println(x); // Output: 15

        x -= 5; // x is now 10
        System.out.println(x); // Output: 10

        x *= 5; // x is now 50
        System.out.println(x); // Output: 50

        x /= 5; // x is now 10
        System.out.println(x); // Output: 10

        x %= 5; // x is now 0
        System.out.println(x); // Output: 0

    }
}
